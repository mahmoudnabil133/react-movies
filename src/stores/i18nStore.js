export const useI18nStore = create(
  persist(
    (set, get) => ({
      lang: 'en',
      languages: LANGUAGES,

      setLang: (newLang) => set({ lang: newLang }),

      t: (key) => {
        const { lang } = get();
        return translations[lang]?.[key] || translations.en?.[key] || key;
      },
    }),
    {
      name: 'i18n',
      partialize: (state) => ({ lang: state.lang }),
    }
  )
);
